using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Entidades
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An usuario. </summary>
    ///Se crea Usuario
    /// <remarks>   José Pablo, 22/04/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public abstract class Usuario
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract int ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the nombre completo. </summary>
        ///
        /// <value> The nombre completo. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string NombreCompleto { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier tipo usuario. </summary>
        ///
        /// <value> The identifier tipo usuario. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract TipoUsuario IDTipoUsuario { get; set; }  

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier nivel. </summary>
        ///
        /// <value> The identifier nivel. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract Nivel IDNivel { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the genero. </summary>
        ///
        /// <value> The genero. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string Genero { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the fecha nacimiento. </summary>
        ///
        /// <value> The fecha nacimiento. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract DateTime FechaNacimiento { get; set; }   

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the correo padre. </summary>
        ///
        /// <value> The correo padre. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string CorreoPadre { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the telefono padre. </summary>
        ///
        /// <value> The telefono padre. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string TelefonoPadre { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the contraseña. </summary>
        ///
        /// <value> The contraseña. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string Contraseña { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a value indicating whether the estado. </summary>
        ///
        /// <value> True if estado, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract bool Estado { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the qr. </summary>
        ///
        /// <value> The qr. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public abstract string QR { get; set; }
      

    }
}
