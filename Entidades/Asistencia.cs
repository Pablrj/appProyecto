using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Entidades
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An asistencia. </summary>
    ///Esta clase se encarga de crear asistencia
    /// <remarks>   José Pablo, 22/04/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Asistencia
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime ID { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier grupo. </summary>
        ///
        /// <value> The identifier grupo. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Grupo IDGrupo { get; set; }//IDGrupo

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the destudiante. </summary>
        ///
        /// <value> The i destudiante. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Usuario IDestudiante { get; set; }//IDUsuarioEstudiante

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a value indicating whether the presente. </summary>
        ///
        /// <value> True if presente, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public bool Presente { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Devuelve una cadena que representa el objeto actual. </summary>
        ///
        /// <remarks>   José Pablo, 22/04/2018. </remarks>
        ///
        /// <returns>   Cadena que representa el objeto actual. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string ToString()
        {
            return "Codigo: "+this.ID + "\r\nGrupo: "+this.IDGrupo +
                "\r\nCedula Hijo: " + this.IDestudiante.ID + "\r\nAsistencia: " + this.Presente;
        }
    }
}
